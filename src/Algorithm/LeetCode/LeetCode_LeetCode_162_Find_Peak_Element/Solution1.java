package Algorithm.LeetCode.LeetCode_LeetCode_162_Find_Peak_Element;

public class Solution1 {
    public int findPeakElement(int[] nums) {
        for (int i = 0; i < nums.length - 1; i++) {
            if (nums[i] > nums[i + 1]) {
                return i;
            }
        }

        return nums.length - 1;
    }
}
